package com.example.clfp4.ListView;

public class CheckListData {
    private String number;
    private String todo;

    public void setNumber(String number) {
        this.number = number;
    }

    public void setTodo(String todo) {
        this.todo = todo ;
    }

    public String getNumber() {
        return this.number;
    }

    public String getTodo() {
        return this.todo ;
    }
}